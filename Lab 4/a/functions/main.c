// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç 0, –µ—Å–ª–∏ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫ —Å–æ —Å—Ç–æ—Ä–æ–Ω–∞–º–∏ a, b, c - —è–≤–ª—è–µ—Ç—Å—è –æ—Å—Ç—Ä–æ—É–≥–æ–ª—å–Ω—ã–º,
// 1 ‚Äî –µ—Å–ª–∏ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω—ã–π, 2 ‚Äî —Ç—É–ø–æ—É–≥–æ–ª—å–Ω—ã–º, -1 ‚Äî –µ—Å–ª–∏ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫
// —Å —Ç–∞–∫–∏–º–∏ —Å—Ç–æ—Ä–æ–Ω–∞–º–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
int getTriangleTypeLength(float a, float b, float c) {
  sort3(&a, &b, &c);
  if (isTriangleExist(a, b, c) == 0)
    return -1;
  float pythagoreanTheorem = a * a + b * b - c * c;
  if (pythagoreanTheorem > EPS)
    return 0;
  else if (dabs(pythagoreanTheorem) <= EPS)
    return 1;
  else
    return 2;
}

