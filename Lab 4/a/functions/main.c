int twoBitHybrid(short sh1, short sh2) {
  int result = 0;
  char shift = 0;
  while (sh1 > 0 || sh2 > 0) {
    result += (sh2 & 1) << shift;
    result += (sh1 & 1) << (shift + 1);
    shift += 2;
    sh2 >>= 1;
    sh1 >>= 1;
  }
  return result;
}

