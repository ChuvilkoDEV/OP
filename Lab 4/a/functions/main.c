int isBinPoly(const int x) {
  int isPoly = 0, tmp = x, afterProcessX = 0;
  while (tmp > 0) {
    if (afterProcessX != 0)
      afterProcessX <<= 1;
    afterProcessX += tmp & 1;
    tmp >>= 1;
  }
  if (x == afterProcessX)
    isPoly = 1;
  return isPoly;
}

