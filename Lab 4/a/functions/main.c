// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç 1, –µ—Å–ª–∏ —á–∏—Å–ª–æ a –ø—Ä–æ—Å—Ç–æ–µ, 0 –≤ –æ–±—Ä–∞—Ç–Ω–æ–º —Å–ª—É—á–∞–µ. –ë–µ–∑ –æ–ø—Ç–∏–º–∏–∑–∞—Ü–∏–π
int isPrimeA(const int a) {
  if (a == 1)
    return 0;
  for (int divider = 2; divider < a; divider++)
    if (a % divider == 0)
      return 0;
  return 1;
}

// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç 1, –µ—Å–ª–∏ —á–∏—Å–ª–æ a –ø—Ä–æ—Å—Ç–æ–µ, 0 –≤ –æ–±—Ä–∞—Ç–Ω–æ–º —Å–ª—É—á–∞–µ. –° –æ–ø—Ç–∏–º–∏–∑–∞—Ü–∏–µ–π
// –ø–µ—Ä–µ–±–æ—Ä–∞ –¥–æ ‚àö ùëÅ.
int isPrimeB(const int a) {
  if (a == 1)
    return 0;
  int sqrtA = (int) sqrt(a);
  for (int divider = 2; divider <= sqrtA; divider++)
    if (a % divider == 0)
      return 0;
  return 1;
}

// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç 1, –µ—Å–ª–∏ —á–∏—Å–ª–æ a –ø—Ä–æ—Å—Ç–æ–µ, 0 - –≤ –æ–±—Ä–∞—Ç–Ω–æ–º —Å–ª—É—á–∞–µ. –° –æ–ø—Ç–∏–º–∏–∑–∞—Ü–∏–µ–π
// –ø–µ—Ä–µ–±–æ—Ä–∞ –¥–æ ‚àö ùëÅ —Å —à–∞–≥–æ–º 2.
int isPrimeC(const int a) {
  if ((a % 2 == 0 || a == 1) && a != 2)
    return 0;
  int sqrtA = (int) sqrt(a);
  for (int divider = 3; divider <= sqrtA; divider += 2)
    if (a % divider == 0)
      return 0;
  return 1;
}

