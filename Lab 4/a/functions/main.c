int deleteOctNumber(int a, const int digit) {
  int afterProcessA = 0, octIndex = 0;
  while (a > 0) {
    int iDigit = a & 7;
    if (iDigit != digit)
      afterProcessA += iDigit << (3 * octIndex++);
    a >>= 3;
  }
  return afterProcessA;
}

