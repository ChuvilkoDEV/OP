int invertHex(int a) {
  int afterProcessA = 0;
  for (int i = lengthN(a, 4) - 1; i >= 0; i--) {
    int iDigit = a & 15;
    afterProcessA += iDigit << (4 * i);
    a >>= 4;
  }
  return afterProcessA;
}

