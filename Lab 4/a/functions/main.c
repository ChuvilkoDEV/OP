int swapPairBites(int a) {
  int afterProcessA = 0, quaIndex = 0;
  while (a > 0) {
    if (a == 1)
      afterProcessA += 1 << (2 * quaIndex++);
    else {
      int iDigit = a & 3;
      if (iDigit == 1 || iDigit == 2)
        iDigit = 3 - iDigit;

      afterProcessA += iDigit << (2 * quaIndex++);
    }
    a >>= 2;
  }
  return afterProcessA;
}

