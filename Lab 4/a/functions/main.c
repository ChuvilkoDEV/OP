// –°–æ—Ä—Ç–∏—Ä—É–µ—Ç –∑–Ω–∞—á–µ–Ω–∏—è a –∏ b –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é.
