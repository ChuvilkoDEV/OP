#include <stdio.h>
#include <math.h>

#define EPS 0.00001

/////////////////
/// 1 –ó–∞–¥–∞–Ω–∏–µ ///
/////////////////

// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –º–æ–¥—É–ª—å –≤–µ—â–µ—Å—Ç–≤–µ–Ω–Ω–æ–≥–æ —á–∏—Å–ª–∞ a
double dabs(const double a) {
  return a >= 0 ? a : -a;
}

/////////////////
/// 2 –ó–∞–¥–∞–Ω–∏–µ ///
/////////////////

// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –Ω–∞–∏–±–æ–ª—å—à–µ–µ —á–∏—Å–ª–æ –∏–∑ –¥–≤—É—Ö –≤–≤–µ–¥–µ–Ω–Ω—ã—Ö —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω—ã—Ö
// –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö a –∏ b
int max2(const int a, const int b) {
  return a > b ? a : b;
}

/////////////////
/// 3 –ó–∞–¥–∞–Ω–∏–µ ///
/////////////////

// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∏–∑ —Ç—Ä–µ—Ö –≤–≤–µ–¥–µ–Ω–Ω—ã—Ö —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω—ã—Ö –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö
// a, b –∏ c
int max3(const int a, const int b, const int c) {
  return max2(max2(a, b), c);
}

/////////////////
/// 4 –ó–∞–¥–∞–Ω–∏–µ ///
/////////////////

// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –º–µ–∂–¥—É –¥–≤—É–º—è —Ç–æ—á–∫–∞–º–∏ —Å –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞–º–∏ (—Ö1, —É1) –∏ (—Ö2, —É2)
double getDistance(const int x1, const int y1,
                   const int x2, const int y2) {
  int dx = abs(x1 - x2); // –¥–µ–ª—å—Ç–∞ x
  int dy = abs(y1 - y2); // –¥–µ–ª—å—Ç–∞ —É
  return sqrt(dx * dx + dy * dy);
}

