// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –¥–ª–∏–Ω—É —á–∏—Å–ª–∞ a, –∑–∞–ø–∏—Å–∞–Ω–Ω–æ–≥–æ –≤ (2^n)-–æ–π —Å–∏—Å—Ç–µ–º–µ —Å—á–∏—Å–ª–µ–Ω–∏—è
int lengthN(int a, const int n) {
  int lengthHex = 0;
  while (a > 0) {
    lengthHex++;
    a >>= n;
  }
  return lengthHex;
}

// –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –±—É–¥–µ—Ç –ø–æ–ª—É—á–µ–Ω–æ, –ø–æ—Å–ª–µ —Ç–æ–≥–æ, –∫–∞–∫ —Ü–∏—Ñ—Ä—ã –≤
