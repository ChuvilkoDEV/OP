int binSequence(int x) {
  int maxSequence = 0, lastSequence = 0;
  while (x > 0) {
    if (x & 1) {
      lastSequence++;
      maxSequence = max2(lastSequence, maxSequence);
    } else
      lastSequence = 0;

    x >>= 1;
  }
  return maxSequence;
}

