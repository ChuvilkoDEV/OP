// 0 (`–õ–æ–∂—å`) - –≤ –æ–±—Ä–∞—Ç–Ω–æ–º —Å–ª—É—á–∞–µ
int isTriangleExist(float a, float b, float c) {
  sort3(&a, &b, &c);
  return a + b > c;
}

